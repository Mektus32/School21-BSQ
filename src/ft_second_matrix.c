#include "ft_list.h"

void	ft_second_matrix(char **a, char **p)
{
	int i;
	int j;

	i = 0;
	while (i < g_row)
	{
		j = 0;
		while (j < g_col)
		{
			p[i][j] = a[i][j];
			j++;
		}
		p[i][j] = '\0';
		i++;
	}
}

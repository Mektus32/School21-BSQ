#include "ft_list.h"

void	ft_print_box(char **p)
{
	int i;
	int j;

	i = 0;
	while (i < g_row)
	{
		j = 0;
		while (j < g_col)
		{
			write(1, &p[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	i = -1;
	while (++i < g_row)
		free(p[i]);
	free(p);
}

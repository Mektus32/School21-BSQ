#include "ft_list.h"

void	ft_fill_box(char **p, int i, int j)
{
	int b;
	int d;

	d = -1;
	while (++d < g_side)
	{
		b = -1;
		while (++b < g_side)
			p[i + d][j + b] = g_c;
	}
}
